public class PlayMethod {

    public void play(){
        System.out.println("playing~");
    }

    public void playWith(String name){
        System.out.println("playing~ with " + name);
    }

    public void sleepWith(String name){
        System.out.println("sleeping~ with " + name);
    }

}
